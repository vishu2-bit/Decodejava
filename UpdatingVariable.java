package basics;

public class UpdatingVariable {
    public static void main(String[] args) {
        int i = 5;
        System.out.println(i);
        i = 10;
        System.out.println(i);
        i = i + 5;
        System.out.println(i);
        i = i - 2;
        System.out.println(i);
        i += 2;
        System.out.println(i);
        i -= 5;
        System.out.println(i);
    }
}
