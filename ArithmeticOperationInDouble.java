package basics;

public class ArithmeticOperationInDouble {
    public static void main(String[] args) {
        double x = 2,y = 13;
        System.out.println(x);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
