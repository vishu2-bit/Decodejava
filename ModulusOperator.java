package basics;


public class ModulusOperator {
    public static void main(String[] args) {
        System.out.println(17%4);
        System.out.println(5%11); //[ a < b = a ]
        System.out.println(11%-5); //[ a % -b = a % b ]
        System.out.println(-11%5); //[ (-a) % b = [-a%b] ]
    }
}
